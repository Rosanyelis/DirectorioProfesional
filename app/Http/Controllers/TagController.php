    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'description' => ['required', 'unique:tags'],
        ],
        [
            'description.required' => 'El campo Nombre es obligatorio',
            'description.unique' => 'El valor del campo Nombre ya existe',
        ]);

        $registro = new Tag();
        $registro->description = $request->description;
        $registro->save();

        return redirect('tags')->with('success', 'Registro Guardado exitosamente');
    }

