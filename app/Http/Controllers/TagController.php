    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Tag  $tag
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $count = DB::table('taggables')->where('tags_id', $id)->count();
        if ($count>0) {
            return redirect('/tags')->with('danger', 'No se puede eliminar el registro, existen negocios este tag.');
        } else {
            Tag::where('id', $id)->delete();
            return redirect('/tags')->with('success', 'Registro Eliminado Exitosamente!');
        }
    }
